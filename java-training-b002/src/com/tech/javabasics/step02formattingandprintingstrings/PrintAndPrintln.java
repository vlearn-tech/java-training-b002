package com.tech.javabasics.step02formattingandprintingstrings;

public class PrintAndPrintln {
	public static void main(String[] args) {

		System.out.print("^");
		System.out.print("My message with only print. No new line should be created after this");
		System.out.print("$");

		System.out.println("\n\n\n");

		System.out.println("^");
		System.out.println("My message with println. Now you should be seeing 3 different lines");
		System.out.println("$");

	}
}
